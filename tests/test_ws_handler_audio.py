@pytest.mark.asyncio
async def test_turn_lock_serializes_overlapping_turns():
    order: list[str] = []

    def chat_func(_user, text):
        import time
        order.append(f"start:{text}")
        time.sleep(0.05)
        order.append(f"end:{text}")
        return {"response": f"resp-{text}"}

    handler = AvatarWebSocketHandler(
        state_machine=ConversationStateMachine(),
        emotion_mapper=EmotionMapper(),
        tts_service=_StubTTS(),
        stt_service=_StubSTT(),
        chat_func=chat_func,
        vad_service=_StubVAD([_Seg(300)]),
    )

    ws1 = _FakeWS([])
    ws2 = _FakeWS([])

    async def do_turn(ws, text):
        await handler._process_text_chat(ws, text, "u")

    await asyncio.gather(do_turn(ws1, "a"), do_turn(ws2, "b"))

    assert order in (["start:a", "end:a", "start:b", "end:b"], ["start:b", "end:b", "start:a", "end:a"])
    assert handler._turn_in_progress is False


