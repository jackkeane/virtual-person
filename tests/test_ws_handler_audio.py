    calls = []

    def _fake_synthesize(self, text: str):
        calls.append(text)
        return (b"RIFF" + text.encode("utf-8"), [{"phoneme": "SIL", "start_ms": 0, "end_ms": 50}])

    monkeypatch.setattr("app.voice.tts_service.FallbackTTSService.synthesize", _fake_synthesize)

    svc = FillerAudioService()
    # Allow background pre-generation thread to run.
    import time as _time
    _time.sleep(0.05)

    audio, phonemes = svc.get_filler("zh-CN")
    assert audio.startswith(b"RIFF")
    assert phonemes
    assert calls


