class TestDetectAudioMime:
    def test_mp3_id3_tag(self):
        assert _detect_audio_mime(b"ID3\x04\x00\x00" + b"\x00" * 10) == "audio/mpeg"

    def test_mp3_sync_word(self):
        assert _detect_audio_mime(b"\xff\xfb\x00\x00") == "audio/mpeg"

    def test_wav_riff(self):
        assert _detect_audio_mime(b"RIFF\x00\x00\x00\x00WAVE") == "audio/wav"

    def test_ogg(self):
        assert _detect_audio_mime(b"OggS\x00\x00") == "audio/ogg"

    def test_webm(self):
        assert _detect_audio_mime(b"\x1aE\xdf\xa3" + b"\x00" * 4) == "audio/webm"

    def test_empty_bytes(self):
        assert _detect_audio_mime(b"") == "audio/wav"

    def test_unknown_magic(self):
        assert _detect_audio_mime(b"\xde\xad\xbe\xef") == "application/octet-stream"

    def test_real_sine_wav(self):
        """WAV bytes generated by FallbackTTS should be detected correctly."""
        n = 4410
        buf = io.BytesIO()
        with wave.open(buf, "wb") as w:
            w.setnchannels(1); w.setsampwidth(2); w.setframerate(22050)
            frames = bytearray()
            for i in range(n):
                amp = int(8000 * math.sin(2 * math.pi * 220 * i / 22050))
                frames.extend(int(amp).to_bytes(2, "little", signed=True))
            w.writeframes(bytes(frames))
        audio = buf.getvalue()
        assert _detect_audio_mime(audio) == "audio/wav"


class TestLazySTTLoading:
    def test_not_loaded_on_init(self):
        """Model must NOT be loaded during __init__ when warmup is disabled."""
        prev = config.stt_warmup_enabled
        config.stt_warmup_enabled = False
        try:
            svc = FasterWhisperSTTService()
            assert svc._model is None
            assert svc._load_attempted is False
        finally:
            config.stt_warmup_enabled = prev

    def test_empty_audio_skips_load(self):
        """Empty audio bytes should return early without triggering model load."""
        prev = config.stt_warmup_enabled
        config.stt_warmup_enabled = False
        try:
            svc = FasterWhisperSTTService()
            result = svc.transcribe(b"")
            assert result == ""
            assert svc._load_attempted is False  # load should NOT be triggered for empty audio
        finally:
            config.stt_warmup_enabled = prev

    def test_transcribe_triggers_load(self):
        """Non-empty audio triggers model load attempt."""
        svc = FasterWhisperSTTService()
        # Provide minimal fake WAV so it doesn't crash before load
        n = 100
        buf = io.BytesIO()
        with wave.open(buf, "wb") as w:
            w.setnchannels(1); w.setsampwidth(2); w.setframerate(16000)
            w.writeframes(b"\x00" * n * 2)
        wav_bytes = buf.getvalue()

        # This may succeed or fail depending on whether whisper model is available
        # but _load_attempted MUST be True after calling transcribe with non-empty audio
        try:
            svc.transcribe(wav_bytes)
        except Exception:
            pass
        assert svc._load_attempted is True


class _FakeWS:
    def __init__(self, messages: list[dict]):
        self._messages = [json.dumps(m) for m in messages]
        self.sent: list[dict] = []

    async def accept(self):
        return None

    async def receive_text(self):
        if self._messages:
            return self._messages.pop(0)
        from fastapi import WebSocketDisconnect
        raise WebSocketDisconnect()

    async def send_json(self, data):
        self.sent.append(data)


